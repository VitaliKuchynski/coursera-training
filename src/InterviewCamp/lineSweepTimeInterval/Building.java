package InterviewCamp.lineSweepTimeInterval;

public class Building {
    int height;
    int start;
    int end;

    public Building(int height, int start, int end) {
        this.height = height;
        this.start = start;
        this.end = end;
    }

    public int getHeight() {
        return height;
    }

    public int getStart() {
        return start;
    }

    public int getEnd() {
        return end;
    }
}
